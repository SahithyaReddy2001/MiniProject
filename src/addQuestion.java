    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // SAVE
        String id = jLabel9.getText();
        String name = jTextField2.getText();
        String opt1 = jTextField3.getText();
        String opt2 = jTextField1.getText();
        String opt3 = jTextField4.getText();
        String opt4 = jTextField5.getText();
        String answer = jTextField6.getText();
        try{
            Connection con = connectionDatabase.getCon();
            PreparedStatement p = con.prepareStatement("insert into question values(?,?,?,?,?,?,?)");
            p.setString(1,id);
            p.setString(2,name);
            p.setString(3,opt1);
            p.setString(4,opt2);
            p.setString(5,opt3);
            p.setString(6,opt4);
            p.setString(7,answer);
            p.executeUpdate();
            JFrame frame = new JFrame();
            frame.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(frame,"Updated Sucessfully");
            setVisible(false);
            new addQuestion().setVisible(true);
        }
        catch(Exception e){
            JFrame frame = new JFrame();
            frame.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(frame,e);
        }
    }//GEN-LAST:event_jButton1MouseClicked

